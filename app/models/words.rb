  def self.generate
    word = ""
    chances = rand(0..10)

    if chances > 8
      word << "#{random(BEGINNINGS)} "
    elsif chances > 7
      word << "#{random(ADJECTIVES)} "
    end

    word << "#{random(ADJECTIVES)} "
    word << random(NOUNS)

    if rand(0..10) > 8 && word.split.length < 3
      word << " #{random(ENDINGS)}"
    end

    word
  end

  def self.random(list)
    list[rand(0..(list.length - 1))].split('-').join(' ')
  end
